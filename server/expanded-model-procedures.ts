export function getModelProcedureCount(): { totalModels: number; totalProcedures: number } {
  const models = Object.keys(expandedModelProcedures).length;
  const procedures = Object.values(expandedModelProcedures).reduce((sum, model: any) => sum + model.procedures.length, 0);
  return { totalModels: models, totalProcedures: procedures };
}

export function getAllModelsWithProcedures() {
  return Object.entries(expandedModelProcedures).map(([key, value]: [string, any]) => ({
    id: key,
    brand: value.brand,
    model: value.model,
    year: value.year,
    engine: value.engine,
    procedureCount: value.procedures.length,
    procedures: value.procedures,
  }));
}
